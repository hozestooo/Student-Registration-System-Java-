    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection(url, username, password_db);
            String id = user.getText();
            String password_db = new String(password.getPassword());

            Statement stm = con.createStatement();

            String sql = "select * from student_registration where id='" + id + "' and password='" + password_db + "'";
            //takes out all column from student_registration database where id and password is found
            ResultSet rs = stm.executeQuery(sql);

            if (rs.next()) {
                JOptionPane.showMessageDialog(this, "Successful login. Welcome " + id);
                switch (rs.getInt(3)) {
                    case 1:
                        //this transfers text input in user textfield into student_menu frame
                        student_tab student_menu = new student_tab(user.getText());
                        student_menu.show();
                        student_menu.setVisible(true);
                        dispose();
                        break;
                    default:
                        JOptionPane.showMessageDialog(this, "Error in accessing database");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Unsuccessful login. Please recheck input.");
                user.setText("");
                password.setText("");
            }
            con.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Unsuccessful login. Error accessing records");
            user.setText("");
            password.setText("");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

